/**
 * Função para obter os melhores prospects brasileiros REAIS
 * Baseada em jogadores verificados da LDB
 */
export function getRealBrazilianProspects() {
  return realBrazilianLDBProspects.map(prospect => ({
    ...prospect,
    // Adiciona informações de contexto
    bio: `${prospect.position} de ${prospect.hometown}, destaque da ${prospect.league}. ${prospect.highlights[0]}.`,
    country: 'Brasil',
    team: prospect.team,
    college: prospect.school,
    // Compatibilidade com sistema existente
    source: 'LDB_Real_Curated'
  }));
}

/**
 * Função para obter os TOP prospects brasileiros
 * Ordenados por potencial e mock draft position
 */
export function getTopRealBrazilianProspects() {
  return getRealBrazilianProspects()
    .sort((a, b) => {
      // Primeiro critério: Mock draft position (menor = melhor)
      if (a.mockDraftPosition !== b.mockDraftPosition) {
        return a.mockDraftPosition - b.mockDraftPosition;
      }
      // Segundo critério: Potential score
      return b.scouting.potential - a.scouting.potential;
    })
    .slice(0, 6); // Top 6 para o grid 2x3
}

/**
 * Busca prospect por ID
 */
export function getRealBrazilianProspectById(id) {
  return realBrazilianLDBProspects.find(prospect => prospect.id === id);
}

/**
 * Filtra prospects por posição
 */
export function getRealBrazilianProspectsByPosition(position) {
  return getRealBrazilianProspects().filter(prospect => prospect.position === position);
}

/**
 * Estatísticas gerais dos prospects brasileiros reais
 */
export function getRealBrazilianProspectsStats() {
  const prospects = getRealBrazilianProspects();
  
  return {
    total: prospects.length,
    firstRound: prospects.filter(p => p.mockDraftPosition <= 30).length,
    trending: prospects.filter(p => p.trending === 'up').length,
    watchlisted: prospects.filter(p => p.watchlisted).length,
    averageAge: (prospects.reduce((sum, p) => sum + p.age, 0) / prospects.length).toFixed(1),
    averagePPG: (prospects.reduce((sum, p) => sum + p.stats.ppg, 0) / prospects.length).toFixed(1),
    verified: prospects.filter(p => p.verified).length
  };
}
