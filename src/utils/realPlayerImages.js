/**
 * Obtém uma imagem específica baseada no nome do jogador
 * @param {string} playerName - Nome do jogador
 * @returns {string|null} - URL da imagem ou null se não encontrada
 */
export function getPlayerImage(playerName) {
  return realPlayerImages[playerName] || null;
}

/**
 * Obtém imagem baseada na posição do jogador
 * @param {string} position - Posição do jogador
 * @param {string} playerName - Nome do jogador (para consistência)
 * @returns {string} - URL da imagem
 */
export function getPositionImage(position, playerName = '') {
  const images = positionSpecificImages[position] || positionSpecificImages['PG'];
  
  // Usa hash do nome para consistência
  const hash = playerName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const index = hash % images.length;
  
  return images[index];
}

/**
 * Obtém retrato profissional baseado no nome (para consistência)
 * @param {string} playerName - Nome do jogador
 * @returns {string} - URL do retrato
 */
export function getProfessionalPortrait(playerName) {
  const hash = playerName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const index = hash % professionalPortraits.length;
  
  return professionalPortraits[index];
}

/**
 * Verifica se uma imagem é genérica (contém equipamentos esportivos)
 * @param {string} imageUrl - URL da imagem
 * @returns {boolean} - Se a imagem é genérica
 */
export function isGenericImage(imageUrl) {
  const genericPatterns = [
    'basketball',
    'ball',
    'sneaker',
    'shoe',
    'equipment',
    'court',
    'hoop',
    'jersey',
    'uniform'
  ];
  
  const urlLower = imageUrl.toLowerCase();
  return genericPatterns.some(pattern => urlLower.includes(pattern));
}
