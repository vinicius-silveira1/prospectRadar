  organizeDatabase() {
    // Organizar por ranking
    this.prospects.sort((a, b) => a.ranking - b.ranking);
    
    // Criar indices para busca rapida
    this.createSearchIndices();
  }

  createSearchIndices() {
    this.indices = {
      byPosition: this.groupBy('position'),
      byRegion: this.groupBy('region'),
      byDraftClass: this.groupBy('draftClass'),
      byTier: this.groupBy('tier'),
      byCollege: this.groupBy('college'),
      byTrending: this.groupBy('trending')
    };
  }

  groupBy(key) {
    return this.prospects.reduce((groups, prospect) => {
      const value = prospect[key];
      if (!groups[value]) {
        groups[value] = [];
      }
      groups[value].push(prospect);
      return groups;
    }, {});
  }

  // M√©todos de consulta otimizados
